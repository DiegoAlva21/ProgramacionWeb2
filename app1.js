let dato1=prompt("Introduce el primer numero: ");
let dato2=prompt("Introduce el segundo numero: ");

let suma = parseInt(dato1) + parseInt(dato2);
console.log("El resulato de la suma es: " +suma);

let resta = parseInt(dato1) - parseInt(dato2);
console.log("El resulato de la resta es: " +resta);

let multiplicacion = parseInt(dato1) * parseInt(dato2);
console.log("El resulato de la multiplicacion es: " +multiplicacion);

let division = parseInt(dato1) / parseInt(dato2);
console.log("El resulato de la division es: " +division);

let porcentaje = parseInt(dato1) * parseInt(dato2)/100
console.log("El resulato del porcentaje es: " +porcentaje);